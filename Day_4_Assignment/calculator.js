// calculator with all basic operations

"use strict"

let num1=15
let num2=6

console.log(`Result of all basic calculator operations of ${num1} and ${num2}`)

//addition
let sum=num1+num2
console.log(`${num1} + ${num2} = ${sum}`)

//subtraction
let sub=num1-num2
console.log(`${num1} - ${num2} = ${sub}`)

//multiplication
let mul=num1*num2
console.log(`${num1} * ${num2} = ${mul}`)

//division
let div=num1/num2
console.log(`${num1} / ${num2} = ${div}`)

//modulus (remainder)
let mod=num1%num2
console.log(`${num1} % ${num2} = ${mod} `)

